function split_Espaço_Array(string) {return string.replace(/\(|\)|\-|/g, "").replace(/&/g, "Ê").replace(/  /g, "").split(" ")}
function split_Underline_Array(string) {return string.replace(/\(|\)|\-|/g, "").replace(/&/g, "Ê").replace(/  /g, "").split("_")}
function captalize_Array(array) {
    array.forEach((string, position)=>{array[position] = string[0].toUpperCase() + string.toLowerCase().substring(1)})
    return array
}
function unshift_Array(array, el1, el2, el3, el4, el5) {array.unshift(el1, el2, el3, el4, el5)}
function filter_Array(array, word) {return array.filter(element=>element != word)}
function join_Array(array) {return array.join("_")}
function captalize_Word(word) {return word[0].toLowerCase() + word.substring(1)}
function captalize_Function(string, el1, el2, el3, el4, el5) {
    string = split_Espaço_Array(string)
    string = captalize_Array(string)
    unshift_Array(string, el1, el2, el3, el4, el5)
    string = filter_Array(string, null)
    string = join_Array(string)
    string = captalize_Word(string)
    string = split_Underline_Array(string)
    string = captalize_Array(string)
    string = join_Array(string)
    string = captalize_Word(string)
    return string
}

